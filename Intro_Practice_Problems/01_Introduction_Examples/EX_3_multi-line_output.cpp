#include <iostream>
using namespace std;

int main()
{
    cout << endl; // print a blank line before program output (optional)

    // The text "Hello World" in double quotes is called a String Literal
    cout << "Hello World" << endl;

    cout << "Hello ";                   // statement without an endline
    cout << "World" << endl;            // statement with an endline

    cout << "Hello "                    // multi-line statement
         << "World " << endl;

    cout << endl; // print a blank line after program output (optional)
    return 0;
}
