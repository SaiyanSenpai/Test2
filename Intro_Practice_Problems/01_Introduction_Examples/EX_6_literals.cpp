#include <iostream>
using namespace std;

int main()
{
    cout << endl;

    // Character literals - single quotations
    cout << 'a' << endl;
    cout << '1' << endl;
    cout << '!' << endl << endl;

    // String literals - double quotations
    cout << "Monday" << endl;
    cout << "Tuesday" << endl;

    cout << endl;
    return 0;
}
