#include <iostream>
using namespace std;

int main() {
    cout << "\n";

    string s = "1";         // strings
    int i = 1;              // integers
    float f = 1.0;          // floating point value
    char c = '1';           // character
    bool b = true;          // boolean value

    cout << s << "\n";
    cout << i << "\n";
    cout << f << "\n";
    cout << c << "\n";
    cout << b << "\n\n";

    cout << (s+s) << "\n";  // concatenation (string addition)
    cout << (i+i) << "\n";  // mathematical addition

    cout << "\n";
    return 0;
}
